package com.baidu.dpop.frame.core.constant;

/**
 * 
 * @ClassName: SymbolConstant <br>
 * @Description: 一个abstract的常量类，定义了标点符号一些常量<br>
 * @author huhailiang <br>
 * @date 2014-10-23 下午 8:10:27 <br>
 * 
 */
public abstract class SymbolConstant {

    /**
     * 逗号","
     */
    public static final String SYMBOL_COMMA = ",";

    /**
     * 句号"."
     */
    public static final String SYMBOL_PERIOD = ".";

    /**
     * 分号";"
     */
    public static final String SYMBOL_SEM = ";";

    /**
     * 井号"#"
     */
    public static final String SYMBOL_POUND = "#";

    /**
     * 减号（中划线）"-"
     */
    public static final String SYMBOL_MINUS = "-";

    /**
     * 加号(正号)"+"
     */
    public static final String SYMBOL_PLUS = "+";

    /**
     * 取模号"%"
     */
    public static final String SYMBOL_MOD = "%";

    /**
     * 等于号"="
     */
    public static final String SYMBOL_EQUAL = "=";

    /**
     * 波浪符"~"
     */
    public static final String SYMBOL_TILDE = "~";

    /**
     * 冒号":"
     */
    public static final String SYMBOL_COLON = ":";

    /**
     * 连字号"-"
     */
    public static final String SYMBOL_HYPHEN = "-";

    /**
     * 星号"*"
     */
    public static final String SYMBOL_STAR = "*";

    /**
     * AT符号"@"
     */
    public static final String SYMBOL_AT = "@";

    /**
     * 左方括号"["
     */
    public static final String SYMBOL_OPEN_BRACKET = "[";

    /**
     * 右方括号"]"
     */
    public static final String SYMBOL_CLOSE_BRACKET = "]";

    /**
     * 左花括号"{"
     */
    public static final String SYMBOL_OPEN_BRACE = "{";

    /**
     * 右花括号"}"
     */
    public static final String SYMBOL_CLOSE_BRAE = "}";

}
